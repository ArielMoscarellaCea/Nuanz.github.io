El formulario de contacto en la web.

Los medios de comunicación habilitados: teléfono, correo electrónico y WhatsApp.

Los datos que podemos solicitar son: nombre, apellidos, correo electrónico, teléfono y cualquier información que el usuario decida proporcionarnos en su mensaje.
