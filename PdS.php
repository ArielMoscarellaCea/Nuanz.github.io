3. Finalidad del tratamiento
<br>
Los datos facilitados se utilizan exclusivamente para:

Atender y responder a las solicitudes de información.

Mantener la comunicación derivada de la relación profesional o comercial solicitada por el usuario.

No utilizamos los datos para enviar comunicaciones comerciales sin el consentimiento previo y expreso del usuario.
<br>
4. Base legal para el tratamiento
<br>
La base legal para el tratamiento de los datos es el consentimiento del usuario, otorgado al ponerse en contacto con <strong>NUANZ</strong> por cualquiera de los medios habilitados.
<br>
5. Conservación de los datos
<br>
Los datos serán conservados únicamente durante el tiempo necesario para dar respuesta a la consulta o mantener la relación establecida. Posteriormente serán eliminados, salvo que exista una obligación legal de conservación.
